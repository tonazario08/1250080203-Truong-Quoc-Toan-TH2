DROP TABLE PHONGBAN    CASCADE CONSTRAINTS PURGE;

-- ============================================================
-- TAO BANG
-- ============================================================

-- ---- Tao bang PHONGBAN (chua co FK truong phong - them sau) ----
CREATE TABLE PHONGBAN (
    MAPHG       NUMBER(3)      CONSTRAINT phongban_maphg_pk PRIMARY KEY,
    TENPHG      NVARCHAR2(30)  NOT NULL,
    TRPHG       NUMBER(9),
    NG_NHANCHUC DATE
);

-- ---- Tao bang NHANVIEN ----
CREATE TABLE NHANVIEN (
    MANV      NUMBER(9)      CONSTRAINT nhanvien_manv_pk  PRIMARY KEY,
    HONV      NVARCHAR2(30),
    TENLOT    NVARCHAR2(10),
    TENNV     NVARCHAR2(10)  NOT NULL,
    NGAYSINH  DATE,
    DCHI      NVARCHAR2(50),
    PHAI      NVARCHAR2(3),
    LUONG     NUMBER(10,2),
    MA_NQL    NUMBER(9)      CONSTRAINT nhanvien_manql_fk REFERENCES NHANVIEN(MANV),
    MAPHG     NUMBER(3)      CONSTRAINT nhanvien_maphg_fk REFERENCES PHONGBAN(MAPHG)
);

-- ---- Them FK truong phong vao PHONGBAN (sau khi da co NHANVIEN) ----
ALTER TABLE PHONGBAN
    ADD CONSTRAINT phongban_trphg_fk
    FOREIGN KEY (TRPHG) REFERENCES NHANVIEN(MANV);

-- ---- Tao bang DIADIEM_PHG ----
CREATE TABLE DIADIEM_PHG (
    MAPHG   NUMBER(3)      CONSTRAINT diadiem_maphg_fk REFERENCES PHONGBAN(MAPHG),
    DIADIEM NVARCHAR2(30),
    CONSTRAINT diadiem_pk PRIMARY KEY (MAPHG, DIADIEM)
);

-- ---- Tao bang DEAN ----
CREATE TABLE DEAN (
    MADA     NUMBER(5)      CONSTRAINT dean_mada_pk   PRIMARY KEY,
    TENDA    NVARCHAR2(30)  NOT NULL,
    DDIEM_DA NVARCHAR2(30),
    MAPHG    NUMBER(3)      CONSTRAINT dean_maphg_fk  REFERENCES PHONGBAN(MAPHG)
);

-- ---- Tao bang PHANCONG ----
CREATE TABLE PHANCONG (
    MANV     NUMBER(9)   CONSTRAINT phancong_manv_fk REFERENCES NHANVIEN(MANV),
    MADA     NUMBER(5)   CONSTRAINT phancong_mada_fk REFERENCES DEAN(MADA),
    THOIGIAN NUMBER(5,1),
    CONSTRAINT phancong_pk PRIMARY KEY (MANV, MADA)
);

-- ---- Tao bang THANNHAN ----
CREATE TABLE THANNHAN (
    MANV     NUMBER(9)      CONSTRAINT thannhan_manv_fk REFERENCES NHANVIEN(MANV),
    TENTN    NVARCHAR2(30),
    PHAI     NVARCHAR2(3),
    NGAYSINH DATE,
    QUANHE   NVARCHAR2(10),
    CONSTRAINT thannhan_pk PRIMARY KEY (MANV, TENTN)
);

