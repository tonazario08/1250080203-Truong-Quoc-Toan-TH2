DROP TABLE THANNHAN    CASCADE CONSTRAINTS PURGE;
DROP TABLE PHANCONG    CASCADE CONSTRAINTS PURGE;
DROP TABLE DEAN        CASCADE CONSTRAINTS PURGE;
DROP TABLE DIADIEM_PHG CASCADE CONSTRAINTS PURGE;
